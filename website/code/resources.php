                <!-- Paris Agreement -->
                <div class="bg-white p-6 rounded-lg shadow-lg">
                    <h3 class="text-2xl font-semibold text-gray-800 mb-4">Paris Agreement (2015)</h3>
                    <p class="text-gray-600 mb-4">
                        <strong>What It Is:</strong> A landmark global climate pact where each country sets its own
                        targets for reducing greenhouse gas emissions, known as Nationally Determined Contributions
                        (NDCs).
                    </p>
                    <p class="text-gray-600 mb-4">
                        <strong>Key Points:</strong>
                    <ul class="list-disc list-inside mb-4">
                        <li>Aims to limit global warming to below 2°C and make efforts to cap it at 1.5°C.</li>
                        <li>Countries must update their NDCs every five years to reflect increased ambition in fighting
                            climate change.</li>
                    </ul>
                    </p>
                    <p class="text-gray-600 mb-4">
                        <strong>Why It Matters:</strong> The Paris Agreement is a critical tool for global climate
                        action, and understanding it helps young leaders advocate for stronger NDCs and hold governments
                        accountable.
                    </p>
                </div>
                <!-- Loss and Damage Fund -->
                <div class="bg-white p-6 rounded-lg shadow-lg">
                    <h3 class="text-2xl font-semibold text-gray-800 mb-4">Loss and Damage Fund (2022)</h3>
                    <p class="text-gray-600 mb-4">
                        <strong>What It Is:</strong> A financial mechanism established at COP27 to provide funding for
                        countries suffering the unavoidable impacts of climate change.
                    </p>
                    <p class="text-gray-600 mb-4">
                        <strong>Key Points:</strong>
                    <ul class="list-disc list-inside mb-4">
                        <li>Provides assistance to countries facing irreversible climate damages such as glacier melts,
                            extreme weather events, and ecosystem loss.</li>
                        <li>Encourages developed countries, who are historically responsible for higher emissions, to
                            contribute significantly to the fund.</li>
                    </ul>
                    </p>
                    <p class="text-gray-600 mb-4">
                        <strong>Why It Matters:</strong> Nepal, with its fragile mountain ecosystems and vulnerable
                        communities, can benefit greatly from this fund to protect its people from the adverse effects
                        of climate change.
                    </p>
                </div>
            </div>
