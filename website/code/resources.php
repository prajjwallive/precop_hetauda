                    <h3 class="text-2xl font-semibold text-gray-800 mb-4">Kyoto Protocol (1997)</h3>
                    <p class="text-gray-600 mb-4">
                        <strong>What It Is:</strong> A legally binding international treaty that set emission reduction
                        targets for industrialized countries.
                    </p>
                    <p class="text-gray-600 mb-4">
                        <strong>Impact:</strong> The protocol’s mechanisms, such as carbon trading and the Clean
